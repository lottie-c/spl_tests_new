package cz.cuni.mff.spl.utils;

/**
 * Class for creating unique IDs for objects transformed to XML. ID is
 * constructed as string "id" (because XML ID have to start with character),
 * then current time in milliseconds (for uniqueness between more runs of the
 * program) and number of constructed IDs (for uniqueness in the current run
 * that can create more IDs in one millisecond).
 * 
 * @author Jaroslav Kotrc
 */
public class IDSupervisor {
    private static int lastId = 0;

    /** Returns unique ID */
    public static String getId() {
        return ("id-" + System.currentTimeMillis() + '-' + lastId++);
    }
}
